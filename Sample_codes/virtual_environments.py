# The module used to create virtual environments is called venv
# To create a virtual environment select / move to the directory of your choice and then run the venv module as a script with the directory name
# Example in my Linux computer to create a virtual env called Sample one I run the following command:

# python3 -m venv Sample_one 
 # To activate the virtual environment, you can use the following command:

# source Sample_one/bin/activate